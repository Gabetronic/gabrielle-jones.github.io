function min(num, num2) {
  //returns the minimum of the two number params - use Math object method Math.min()
  return Math.min(num, num2);
  }

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num){
//if the number param is even, 
  if(num % 2 === 0){
    //return true,
    return true;
  }
//else, return false
return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, target){
  //Initialize new variable to 0,
  let count = 0;
    //Iterate through string
    for(var i = 0; i < string.length; i++){
      //If target === interation, 
      if(string[i] === target){
        //add to the tally
        count += 1;
      }
    }
  //Return tally
  return count;
}
  

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string){
  //Return the number of capital "B"s in the string parameter:
    return countChars(string, "B");
  }

